<section id="section-1">
	<h2>Getting started with the Clembs SMP</h2>

	<h3>Installation</h3>
	<ol>
		<li>
			<a href="https://www.minecraft.net/store/checkout/minecraft-java-bedrock-edition-pc">
				Purchase the game
			</a>
			or get a cracked copy, then install it (on version 1.20.1).
		</li>
		<li>
			For best gameplay, install a
			<a href="https://modrinth.com/modpack/fabulously-optimized/versions#all-versions">
				performance modpack
			</a>
			(if you own a graphics card) or <a href="https://optifine.net/downloads">OptiFine</a> (if you don't).
		</li>

		<li>Launch the game from your launcher, then go to "Multiplayer".</li>
		<li>
			Click "Add Server" and enter
			<code>smp.clembs.com</code> in the address field.
		</li>
		<li>Click "Done", then double-click the newly created server.</li>

		<li>
			You'll be prompted to create a password, to prevent data theft. Once that's made, log in and
			you're in!
		</li>
	</ol>
</section>
